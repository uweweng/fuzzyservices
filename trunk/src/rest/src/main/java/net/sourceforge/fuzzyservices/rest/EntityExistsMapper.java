package net.sourceforge.fuzzyservices.rest;

import javax.ws.rs.core.Response;
import javax.ws.rs.ext.ExceptionMapper;
import javax.ws.rs.ext.Provider;

@Provider
public class EntityExistsMapper implements
        ExceptionMapper<javax.persistence.EntityExistsException> {

    @Override
    public Response toResponse(javax.persistence.EntityExistsException ex) {
        return Response.status(404).
                entity(ex.getMessage()).
                type("text/plain").
                build();
    }
}